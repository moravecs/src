Google+</a>. Serás el primero en enterarte.
</div>
</li>
<li class="accordion">
<a class="accordion-heading">
<h4 class="accordion-title"> ¿Qué futuro me espera si no tengo unos estudios mínimos?</h4>
<span class="fa fa-plus-circle pull-right"></span>
</a>
<div class="accordion-body">
El Informe PIACC sobre Educación de Adultos de la OCDE señala que los que abandonan la formación sin finalizar la formación mínima en su edad escolar tienen mayores dificultades para volver a formarse y encontrar un trabajo más cualificado.<br /> 
Si es tu caso, con un poco de esfuerzo, podrás completar tus estudios básicos y obtener la certificación para poder seguir estudiando, encontrar un trabajo mejor o poder optar a realizar una oposición.
