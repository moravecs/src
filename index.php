<div class="col-sm-12">
<div class="section-header text-center">
<h2>Preguntas Frecuentes</h2>
<h4>Si tienes dudas, esperamos que esto pueda ayudarte.</h4>
</div>
</div>
<div class="col-sm-6">
<ul class="accordion-group">
<li class="accordion">
<a class="accordion-heading">
<h4 class="accordion-title">¿Qué es la Educación Secundaria Obligatoria?</h4>
<span class="fa fa-plus-circle pull-right"></span>
</a>
<div class="accordion-body">
La Educación Secundaria Obligatoria (ESO) se convirtió en la estructura educativa para la enseñanza secundaria a partir de 1996. Su objetivo es la preparción de los alumnos comprendidos entre los 12 y 16 años para que puedan acceder al siguiente nivel educativo o al mercado laboral. Puedes realizarla en los IES (Institutos de Educación Secundaria), en los IESO (Institutos de Educación Secundaria Obligatoria), en los Centros o Colegios de Enseñanza Obligatoria (CEO), en centros privados y concertados o bien en centros para mayores si tienes más de 18 años. Tanto la LOE de 2006 y la LOMCE de 2013 han modificado su configuración inicial.
</div>
</li>
<li class="accordion">
<a class="accordion-heading">
<h4 class="accordion-title">¿Cuales son las características de la ESO?</h4>
<span class="fa fa-plus-circle pull-right"></span>
</a>
<div class="accordion-body">
<span class="fa fa-check"> Para algunos alumnos supone el abandono de sus estudios y para otros es propedéutica, les permite continuar estudiando</span><br/>
<span class="fa fa-check"> Te prepara para la incorporación al mercado laboral o para tu educación adulta.</span><br />
<span class="fa fa-check"> Es común a todos los alumnos lo que la convierte en una etápa integral.</span><br />
<span class="fa fa-check"> Los cursos disponen de asignaturas comunes y optativas que son específicas para algunos alumnos y que dependen de cada centro.</span><br />
<span class="fa fa-check"> Está estructurada en cuatro cursos.</span>
</div>
</li>
<li class="accordion">
<a class="accordion-heading">
<h4 class="accordion-title"> ¿Qué es la Educación para adultos?</h4>
<span class="fa fa-plus-circle pull-right"></span>
</a>
<div class="accordion-body">
La educación para adultos ofrece a todos los mayores de dieciocho años o más la posibilidad de conseguir, mejorar, cumplimentar o hacer crecer sus conocimientos y aptitudes para su desarrollo dentro del ámbito personal y el profesional. Excepcionalmente los mayores de dieciséis años podrán realizar estos estudios.<br /><br />
Esta formación busca insertarte en el proceso de reciclaje y aprendizaje durante tu vida, continuando y mejorando tu formación.
</div>
</li>
<li class="accordion">
<a class="accordion-heading">
<h4 class="accordion-title"> ¿Puedo sacarme un certificado de profesionalidad sin la ESO?</h4>
<span class="fa fa-plus-circle pull-right"></span>
</a>
<div class="accordion-body">
La mayoría de los certificados de profesionalidad, que sirven para certificar que tienes la experiencia suficiente para desarrollar un cierto trabajo y que te otorgan un título acreditativo, requieren que dispongas del título de Graduado para optar a obtenerlo.
</div>
</li>
<li class="accordion">
<a class="accordion-heading">
<h4 class="accordion-title"> ¿Qué opciones tengo para sacarme el título?</h4>
<span class="fa fa-plus-circle pull-right"></span>
</a>
<div class="accordion-body">
Si eres mayor de 18 años tienes, básicamente, tres opciones:<br />
<span class="fa fa-check"> Acudir a un centro para mayores y asistir y superar los cursos que realizan.</span><br />
<span class="fa fa-check"> Prepararte por tu cuenta o mediante un curso a distancia, presentarte a las pruebas libres que organizan las comunidades autónomas anualmente, aprobar y obtener el título.</span><br />
<span class="fa fa-check"> Superar este curso y obtener directamente el título.</span><br />
</div>
</li>
<li class="accordion">
<a class="accordion-heading">
<h4 class="accordion-title"> Finalidad de la ESO</h4>
<span class="fa fa-plus-circle pull-right"></span>
</a>
<div class="accordion-body">
Su finalidad es conseguir que adquieras las nociones culturales básicas dentro de la humanística, el arte, la ciencia y la tecnología. Para ello: <br />
<span class="fa fa-check"> Deberás despertar y fortalecer buenos hábitos para estudiar y trabajar.</span><br />
<span class="fa fa-check"> Prepararte para acceder a estudios superirores y para tu inserción en el mundo laboral.</span><br />
<span class="fa fa-check"> Formárte como ciudadano, con todos sus derechos y obligaciones.</span><br />
</div>
</li>
</ul>
</div>
<div class="col-sm-6">
<ul class="accordion-group">
<li class="accordion">
<a class="accordion-heading">
<h4 class="accordion-title">¿Qué es la Educación secundaria?</h4>
<span class="fa fa-plus-circle pull-right"></span>
</a>
<div class="accordion-body">
La educación secundaria está compuesta por la Educación Secundaria Obligatoria (ESO) y educación secundaria postobligatoria.<br /><br />
Accedes a la educación secundaria postobligatoria una vez has obtenido tu título de Graduado y podrás elegir entre el Bachillerato o la Formación Profesional (FP) de grado medio o FP I que incluye las disciplinas artísticas, plásticas y deportivas. Estos estudios se cursan en Institutos de Enseñanza Secundaria (IES) o a distancia.<br />
</div>
</li>
<li class="accordion">
<a class="accordion-heading">
<h4 class="accordion-title">¿Qué Titulación obtengo?</h4>
<span class="fa fa-plus-circle pull-right"></span>
</a>
<div class="accordion-body">
Una vez finalizado el cursos, obtendrás la Titulación Oficial de Graduado en Eduación Secundaria, equivalente al Graduado Escolar. El centro que imparte el curso es un centro educativo para adultos oficial, lo que hace no tengas que realizar ninguna prueba extra para oficiliazarlo.
</div>
</li>
<li class="accordion">
<a class="accordion-heading">
